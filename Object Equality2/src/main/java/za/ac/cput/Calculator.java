package za.ac.cput;

public class Calculator {

    public int add(int a,int b) {

        return a+b;
    }

    public int diff(int a,int b){
        return b/a;
    }

    public int multiply(int a,int b){
        return a*b;
    }

    public int mod(int a ,int b){
        return b%a;
    }

    public int min(int a ,int b){
        return b-a;
    }

}
